def main() -> None:
    for path, extra in ((EN_PATH, EXTRA_EN), (AR_PATH, EXTRA_AR)):
        data = json.loads(path.read_text(encoding="utf-8"))
        overlap = set(data) & set(extra) - set()
        if overlap:
            raise SystemExit(f"Keys already exist in {path.name}: {sorted(overlap)}")
        data.update(extra)
        path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print("Merged", len(EXTRA_EN), "keys into app_en.arb and app_ar.arb")


if __name__ == "__main__":
    main()
